var GmSSL = require("./index");
var lib = require('./src/lib')

var hashval = GmSSL.SM3.hash("hello world");
console.log(lib.uint8tohex(hashval))
var sm4key = GmSSL.SM4.set_encrypt_key([
  0x01, 0x23, 0x45, 0x67, 0x89, 0xab, 0xcd, 0xef, 0xfe, 0xdc, 0xba, 0x98, 0x76,
  0x54, 0x32, 0x10,
]);
var encrypted = GmSSL.SM4.encrypt(sm4key, [
    0x31,0x32,0x33,0x34,0x35,0x36,0x37,0x38,
    0x31,0x32,0x33,0x34,0x35,0x36,0x37,0x38])
    
console.log(lib.uint8tohex(encrypted));
var sm4deckey = GmSSL.SM4.set_decrypt_key([
  0x01, 0x23, 0x45, 0x67, 0x89, 0xab, 0xcd, 0xef, 0xfe, 0xdc, 0xba, 0x98, 0x76,
  0x54, 0x32, 0x10,
]);
decrypted = GmSSL.SM4.decrypt(sm4deckey,encrypted)
console.log(lib.uint8tohex(decrypted));
